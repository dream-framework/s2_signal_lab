def save_json(path: Path, obj: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(obj, indent=2, sort_keys=False), encoding="utf-8")


def fetch_all_quotes(tickers: list[str], args: argparse.Namespace) -> tuple[list[dict[str, Any]], list[dict[str, str]]]:
    all_rows: list[dict[str, Any]] = []
    failures: list[dict[str, str]] = []
    log(f"[INFO] Fetching real quote history for {len(tickers)} tickers using sources={','.join(args.sources)}")
    log(f"[INFO] First 30 tickers: {', '.join(tickers[:30])}")
    done = 0
    with ThreadPoolExecutor(max_workers=args.workers) as pool:
        future_map = {pool.submit(fetch_symbol, ticker, args): ticker for ticker in tickers}
        for future in as_completed(future_map):
            ticker = future_map[future]
            done += 1
            try:
                result = future.result()
            except Exception as exc:
                result = FetchResult(ticker, [], None, str(exc))
            if result.rows:
                all_rows.extend(result.rows)
            else:
                failures.append({"ticker": ticker, "error": result.error or "no rows"})
            if done % args.progress_every == 0 or done == len(tickers):
                ok = done - len(failures)
                log(f"[INFO] Progress {done}/{len(tickers)}; ok={ok}; failed={len(failures)}")
    return all_rows, failures


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="Fetch real market data and build market signal analysis outputs.")
    parser.add_argument("--universe", choices=["us-all", "seed", "anchors", "custom"], default=os.getenv("MARKET_UNIVERSE", "us-all"))
    parser.add_argument("--tickers-file", default=os.getenv("MARKET_TICKERS_FILE", "data/tickers.csv"))
    parser.add_argument("--output-dir", default=os.getenv("MARKET_OUTPUT_DIR", "docs/data"))
    parser.add_argument("--html", default=os.getenv("MARKET_HTML", "docs/index.html"))
    parser.add_argument("--period", default=os.getenv("MARKET_PERIOD", "1y"), help="Yahoo chart range, e.g. 6mo, 1y, 2y, 5y")
    parser.add_argument("--interval", default=os.getenv("MARKET_INTERVAL", "1d"), help="Yahoo chart interval, e.g. 1d")
    parser.add_argument("--sources", default=os.getenv("MARKET_SOURCES", "yahoo,stooq"), help="Comma list: yahoo,stooq")
    parser.add_argument("--workers", type=int, default=int(os.getenv("MARKET_WORKERS", "12")))
    parser.add_argument("--retries", type=int, default=int(os.getenv("MARKET_RETRIES", "2")))
    parser.add_argument("--timeout", type=int, default=int(os.getenv("MARKET_TIMEOUT", "30")))
    parser.add_argument("--max-tickers", type=int, default=int(os.getenv("MARKET_MAX_TICKERS", "0")), help="0 means no cap")
    parser.add_argument("--min-history-days", type=int, default=int(os.getenv("MARKET_MIN_HISTORY_DAYS", "60")))
    parser.add_argument("--progress-every", type=int, default=int(os.getenv("MARKET_PROGRESS_EVERY", "100")))
    parser.add_argument("--include-special", action="store_true", help="include warrants, units, rights, preferreds from listing files")
    parser.add_argument("--strict-universe", action="store_true", help="fail if live universe files cannot be read")
    args = parser.parse_args(argv)
    args.sources = [s.strip().lower() for s in str(args.sources).split(",") if s.strip()]

    out_dir = Path(args.output_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    tickers, universe_source, source_note = resolve_universe(args)
    if not tickers:
        log("[ERROR] Universe resolved to zero tickers.")
        return 2
    if source_note:
        log(f"[WARN] {source_note}")

    write_csv(out_dir / "universe.csv", [{"ticker": t} for t in tickers], ["ticker"])
    all_rows, failures = fetch_all_quotes(tickers, args)
    if not all_rows:
        log("[ERROR] No real market quote rows were fetched. Refusing to create simulated/fake outputs.")
        save_json(out_dir / "fetch_manifest.json", {
            "generated_at_utc": dt.datetime.now(dt.timezone.utc).replace(microsecond=0).isoformat(),
            "universe_source": universe_source,
            "requested_tickers": len(tickers),
            "successful_tickers": 0,
            "failed_tickers": len(failures),
            "failures": failures[:500],
        })
        return 3

    all_rows.sort(key=lambda r: (r.get("ticker") or "", r.get("date") or ""))
    quote_fields = ["date", "ticker", "open", "high", "low", "close", "adj_close", "volume", "source"]
    write_csv(out_dir / "quotes_long.csv", all_rows, quote_fields)

    signals = compute_latest_signals(all_rows, args.min_history_days)
    if not signals:
        log("[ERROR] Quotes were fetched, but no ticker had enough real history for signal analysis.")
        return 4

    signal_fields = [
        "ticker", "last_date", "last_close", "rows", "source", "signal_label", "market_signal_score",
        "ret_1d", "ret_5d", "ret_20d", "ret_63d", "ret_126d", "ret_252d",
        "sma20", "sma50", "sma200", "above_sma20", "above_sma50", "above_sma200",
        "distance_sma50", "distance_sma200", "realized_vol_20d", "rsi14", "drawdown_252d",
        "near_20d_high", "near_20d_low", "distance_52w_high", "distance_52w_low", "avg_volume_20d",
        "rank_ret_5d", "rank_ret_20d", "rank_ret_63d", "rank_ret_126d", "rank_low_vol_20d", "rank_drawdown_252d",
    ]
    write_csv(out_dir / "signals_latest.csv", signals, signal_fields)

    successful = len({row["ticker"] for row in all_rows})
    manifest = {
        "generated_at_utc": dt.datetime.now(dt.timezone.utc).replace(microsecond=0).isoformat(),
        "universe_source": universe_source,
        "universe_note": source_note,
        "requested_tickers": len(tickers),
        "successful_tickers": successful,
        "failed_tickers": len(failures),
        "quote_rows": len(all_rows),
        "signals_count": len(signals),
        "period": args.period,
        "interval": args.interval,
        "sources": args.sources,
        "failures": failures[:1000],
    }
    save_json(out_dir / "fetch_manifest.json", manifest)
    summary = build_summary(signals, manifest)
    save_json(out_dir / "market_summary.json", summary)
    if args.html:
        write_html_report(Path(args.html), summary)

    log(f"[INFO] Completed. requested={len(tickers)} successful={successful} failed={len(failures)} signals={len(signals)}")
    log(f"[INFO] Wrote {out_dir / 'signals_latest.csv'} and {out_dir / 'market_summary.json'}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
