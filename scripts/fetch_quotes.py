def normalize_symbol(symbol: str) -> str:
    value = (symbol or "").strip().upper()
    if not value:
        return ""
    # Nasdaq files sometimes use class dots; Yahoo uses dashes.
    value = value.replace(".", "-")
    return value


def dedupe_preserve_order(items: list[str]) -> list[str]:
    seen: set[str] = set()
    out: list[str] = []
    for item in items:
        sym = normalize_symbol(item)
        if not sym or sym in seen:
            continue
        seen.add(sym)
        out.append(sym)
    return out


def request_text(url: str, timeout: int = 30) -> str:
    response = requests.get(url, headers={"User-Agent": USER_AGENT}, timeout=timeout)
    response.raise_for_status()
    text = response.text
    if not text or len(text.strip()) < 20:
        raise RuntimeError(f"empty/short response from {url}")
    return text


def parse_nasdaq_pipe_file(text: str, source: str, include_special: bool) -> list[str]:
    # Nasdaq Trader appends a footer line; DictReader will expose it as a row
    # with Symbol/ACT Symbol equal to "File Creation Time". Skip it explicitly.
    reader = csv.DictReader(io.StringIO(text), delimiter="|")
    if not reader.fieldnames:
        raise RuntimeError(f"{source}: missing header")
    fields = set(reader.fieldnames)
    if "Symbol" in fields:
        symbol_field = "Symbol"
    elif "ACT Symbol" in fields:
        symbol_field = "ACT Symbol"
    else:
        raise RuntimeError(f"{source}: unexpected header {reader.fieldnames}")

    symbols: list[str] = []
    for row in reader:
        raw_symbol = (row.get(symbol_field) or "").strip()
        if not raw_symbol or raw_symbol.startswith("File Creation Time"):
            continue
        if (row.get("Test Issue") or "").strip().upper() == "Y":
            continue
        name = (row.get("Security Name") or "").strip().lower()
        if not include_special and any(tok in f" {name} " for tok in SPECIAL_NAME_TOKENS):
            continue
        sym = normalize_symbol(raw_symbol)
        if sym and len(sym) <= 12:
            symbols.append(sym)
    return symbols


def fetch_us_all_universe(include_special: bool = False) -> list[str]:
    symbols: list[str] = []
    for url, source in [(NASDAQ_LISTED_URL, "nasdaqlisted"), (OTHER_LISTED_URL, "otherlisted")]:
        text = request_text(url)
        symbols.extend(parse_nasdaq_pipe_file(text, source, include_special))
    return dedupe_preserve_order(ANCHOR_TICKERS + symbols)


def load_csv_tickers(path: Path) -> list[str]:
    if not path.exists():
        return []
    out: list[str] = []
    with path.open("r", newline="", encoding="utf-8-sig") as f:
        sample = f.read(4096)
        f.seek(0)
        try:
            dialect = csv.Sniffer().sniff(sample) if sample.strip() else csv.excel
        except csv.Error:
            dialect = csv.excel
        reader = csv.DictReader(f, dialect=dialect)
        if reader.fieldnames:
            field_map = {name.lower().strip(): name for name in reader.fieldnames}
            ticker_col = field_map.get("ticker") or field_map.get("symbol") or reader.fieldnames[0]
            for row in reader:
                out.append(row.get(ticker_col, ""))
        else:
            f.seek(0)
            for row in csv.reader(f, dialect=dialect):
                if row:
                    out.append(row[0])
    return dedupe_preserve_order(out)


def resolve_universe(args: argparse.Namespace) -> tuple[list[str], str, str | None]:
    extra = load_csv_tickers(Path(args.tickers_file)) if args.tickers_file else []
    source_note = None
    if args.universe == "custom":
        tickers = extra
        source = f"custom:{args.tickers_file}"
    elif args.universe == "seed":
        tickers = dedupe_preserve_order(FALLBACK_TICKERS + extra)
        source = "built-in broad seed"
    elif args.universe == "anchors":
        tickers = dedupe_preserve_order(ANCHOR_TICKERS + extra)
        source = "cross-asset anchors"
    else:
        try:
            live = fetch_us_all_universe(include_special=args.include_special)
            tickers = dedupe_preserve_order(live + extra)
            source = "Nasdaq Trader listed symbols + anchors"
        except Exception as exc:
            if args.strict_universe:
                raise
            source_note = f"Universe live source failed; used broad built-in fallback. Error: {exc}"
            tickers = dedupe_preserve_order(FALLBACK_TICKERS + extra)
            source = "built-in broad seed fallback"

    if args.max_tickers and args.max_tickers > 0:
        tickers = tickers[: args.max_tickers]
        source_note = (source_note + " " if source_note else "") + f"Universe capped at {args.max_tickers}."
    return tickers, source, source_note


@dataclass
class FetchResult:
    ticker: str
    rows: list[dict[str, Any]]
    source: str | None = None
    error: str | None = None


def fetch_yahoo_chart(symbol: str, period: str, interval: str, timeout: int) -> FetchResult:
    encoded = quote(symbol, safe="")
    url = YAHOO_CHART_URL.format(symbol=encoded)
    params = {
        "range": period,
        "interval": interval,
        "includePrePost": "false",
        "events": "div,splits",
    }
    response = requests.get(url, params=params, headers={"User-Agent": USER_AGENT}, timeout=timeout)
    if response.status_code >= 400:
        snippet = response.text[:180].replace("\n", " ")
        raise RuntimeError(f"Yahoo HTTP {response.status_code}: {snippet}")
    try:
        payload = response.json()
    except Exception as exc:
        snippet = response.text[:240].replace("\n", " ")
        raise RuntimeError(f"Yahoo non-JSON response: {exc}; first chars: {snippet}")

    chart = payload.get("chart") or {}
    error = chart.get("error")
    if error:
        raise RuntimeError(f"Yahoo chart error: {error}")
    results = chart.get("result") or []
    if not results:
        raise RuntimeError("Yahoo chart returned no result")
    result = results[0]
    timestamps = result.get("timestamp") or []
    quote_blocks = ((result.get("indicators") or {}).get("quote") or [])
    if not timestamps or not quote_blocks:
        raise RuntimeError("Yahoo chart returned no timestamp/quote data")
    quote_block = quote_blocks[0]
    adj_block = (((result.get("indicators") or {}).get("adjclose") or [{}])[0])

    opens = quote_block.get("open") or []
    highs = quote_block.get("high") or []
    lows = quote_block.get("low") or []
    closes = quote_block.get("close") or []
    volumes = quote_block.get("volume") or []
    adjcloses = adj_block.get("adjclose") or []

    rows: list[dict[str, Any]] = []
    for i, ts in enumerate(timestamps):
        close = closes[i] if i < len(closes) else None
        if close is None:
            continue
        date = dt.datetime.fromtimestamp(int(ts), tz=dt.timezone.utc).date().isoformat()
        rows.append({
            "date": date,
            "ticker": symbol,
            "open": opens[i] if i < len(opens) else None,
            "high": highs[i] if i < len(highs) else None,
            "low": lows[i] if i < len(lows) else None,
            "close": close,
            "adj_close": adjcloses[i] if i < len(adjcloses) else close,
            "volume": volumes[i] if i < len(volumes) else None,
            "source": "yahoo_chart",
        })
    if not rows:
        raise RuntimeError("Yahoo chart contained no usable close rows")
    return FetchResult(symbol, rows, "yahoo_chart")


def stooq_symbol(symbol: str) -> str:
    value = symbol.lower()
    if value.startswith("^") or "-usd" in value:
        return value
    if "." not in value:
        value = f"{value}.us"
    return value


def fetch_stooq_daily(symbol: str, timeout: int) -> FetchResult:
    url = STOOQ_DAILY_URL.format(symbol=quote(stooq_symbol(symbol), safe=""))
    response = requests.get(url, headers={"User-Agent": USER_AGENT}, timeout=timeout)
    if response.status_code >= 400:
        snippet = response.text[:180].replace("\n", " ")
        raise RuntimeError(f"Stooq HTTP {response.status_code}: {snippet}")
    text = response.text
    if "<html" in text[:300].lower():
        raise RuntimeError(f"Stooq returned HTML, first chars: {text[:180].replace(chr(10), ' ')}")
    reader = csv.DictReader(io.StringIO(text))
    expected = {"Date", "Open", "High", "Low", "Close", "Volume"}
    if not reader.fieldnames or not expected.issubset(set(reader.fieldnames)):
        raise RuntimeError(f"Stooq unexpected CSV header: {reader.fieldnames}; first chars: {text[:180].replace(chr(10), ' ')}")
    rows: list[dict[str, Any]] = []
    for row in reader:
        try:
            close = float(row["Close"])
        except Exception:
            continue
        rows.append({
            "date": row["Date"],
            "ticker": symbol,
            "open": to_float(row.get("Open")),
            "high": to_float(row.get("High")),
            "low": to_float(row.get("Low")),
            "close": close,
            "adj_close": close,
            "volume": to_float(row.get("Volume")),
            "source": "stooq_daily",
        })
    if not rows:
        raise RuntimeError("Stooq CSV contained no usable rows")
    return FetchResult(symbol, rows, "stooq_daily")


def to_float(value: Any) -> float | None:
    if value is None or value == "":
        return None
    try:
        v = float(value)
    except Exception:
        return None
    if math.isfinite(v):
        return v
    return None


def fetch_symbol(symbol: str, args: argparse.Namespace) -> FetchResult:
    last_error = None
    for attempt in range(1, args.retries + 2):
        for source in args.sources:
            try:
                if source == "yahoo":
                    return fetch_yahoo_chart(symbol, args.period, args.interval, args.timeout)
                if source == "stooq":
                    return fetch_stooq_daily(symbol, args.timeout)
                raise RuntimeError(f"unknown source {source}")
            except Exception as exc:
                last_error = f"{source}: {exc}"
        if attempt <= args.retries:
            time.sleep(min(2.0 * attempt, 8.0))
    return FetchResult(symbol, [], None, last_error or "unknown fetch error")


